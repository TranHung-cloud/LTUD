-- TẠO BẢNG
CREATE TABLE COKHAUHAO (
   MATAISAN CHAR(10) NOT NULL,
   NAMKHAUHAO INT NOT NULL,
   GIATRIKHAUHAO FLOAT,
   GIATRICONLAISAUKHAUHAO FLOAT,
   CONSTRAINT PK_COKHAUHAO PRIMARY KEY (MATAISAN, NAMKHAUHAO)
);

CREATE TABLE GIADINH (
   MAGIADINH CHAR(10) NOT NULL PRIMARY KEY,
   TENGIADINH NVARCHAR(256) NOT NULL
);

CREATE TABLE PHUONGPHAPKHAUHAO (
   MAPP CHAR(10) NOT NULL PRIMARY KEY,
   TENPHUONGPHAP NVARCHAR(256) NOT NULL,
   TYLE FLOAT
);

CREATE TABLE LOAITAISAN (
   MALOAI CHAR(10) NOT NULL PRIMARY KEY,
   MAPP CHAR(10) NOT NULL,
   TENLOAI NVARCHAR(256) NOT NULL,
   SONAMSUDUNG INT NOT NULL,
   CHUKYBAOTRI SMALLINT NOT NULL
);

CREATE TABLE VAITRO (
   MAVAITRO CHAR(10) NOT NULL PRIMARY KEY,
   TENVAITRO NVARCHAR(256) NOT NULL
);

CREATE TABLE NGUOIDUNG (
   MANGUOIDUNG CHAR(10) NOT NULL PRIMARY KEY,
   MAVAITRO CHAR(10) NOT NULL,
   MAGIADINH CHAR(10),
   HOTEN NVARCHAR(256) NOT NULL,
   NGAYSINH DATE NOT NULL,
   MATKHAU NVARCHAR(256),
   TRANGTHAI NVARCHAR(256)
);

CREATE TABLE TAISAN (
   MATAISAN CHAR(10) NOT NULL PRIMARY KEY,
   MANGUOIDUNG CHAR(10) NOT NULL,
   MALOAI CHAR(10) NOT NULL,
   TENTAISAN NVARCHAR(256) NOT NULL,
   NGAYMUA DATETIME NOT NULL,
   NGUYENGIA FLOAT NOT NULL,
   TINHTRANG NVARCHAR(50) NOT NULL,
   PHAMVI NVARCHAR(256),
   HINHANH NVARCHAR(256)
);

CREATE TABLE THOIDIEMKHAUHAO (
   NAMKHAUHAO INT NOT NULL PRIMARY KEY
);

CREATE TABLE THONGTINBAOTRI (
   MALICH CHAR(10) NOT NULL PRIMARY KEY,
   MATAISAN CHAR(10) NOT NULL,
   NGAYBAOTRI DATE NOT NULL,
   TRANGTHAI NVARCHAR(50) NOT NULL,
   CHIPHI FLOAT NOT NULL,
   NOIDUNGBAOTRI NVARCHAR(256) NOT NULL
);


